public class VaryingVariables {
	public static String chickens = "Chickens:";
	public static int three = 3;
	public static String bacon = "Bacon (kg):";
	public static double fivePointFive = 5.5;
	public static String tractor = "A Tractor:";
	public static String noTractor = "There is none:";


	public static void main(String[] args) {
		VaryingVariables test = new VaryingVariables();
		System.out.println(test.chickens);
		System.out.println(chickens);
		System.out.println(three);
		System.out.println(bacon);
		System.out.println(fivePointFive);
		System.out.println(tractor);
		System.out.println(noTractor);

	}
}